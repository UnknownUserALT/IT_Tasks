#!/bin/bash

# Проверяем, передан ли аргумент
if [ $# -eq 0 ]; then
    echo "Ошибка: Необходимо передать число в качестве аргумента"
    echo "Пример использования: ./factorial.sh 5"
    exit 1
fi

# Проверяем, что аргумент - неотрицательное число
if ! [[ $1 =~ ^[0-9]+$ ]]; then
    echo "Ошибка: Аргумент должен быть неотрицательным целым числом"
    exit 1
fi

# Получаем число из аргумента
number=$1
factorial=1

# Вычисляем факториал с помощью цикла for
for (( i=1; i<=number; i++ )); do
    factorial=$((factorial * i))
done

# Выводим результат
echo "Факториал числа $number равен $factorial"