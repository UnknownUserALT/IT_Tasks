#include <iostream>
using namespace std;

int sumch() {
    int n;
    int sum = 0;

    // Ввод числа n
    cout << "Введите число n: ";
    cin >> n;

    // Проверка корректности ввода
    if (n <= 0) {
        cout << "Ошибка: n должно быть положительным числом!" << endl;
        return 1;
    }

    // Вычисление суммы чисел, делящихся на 2 или на 5
    for (int i = 1; i <= n; i++) {
        if (i % 2 == 0 || i % 5 == 0) {
            sum += i;
        }
    }

    // Вывод результата
    cout << "Сумма чисел от 1 до " << n << ", которые делятся на 2 или на 5: " << sum << endl;

    return 0;
}